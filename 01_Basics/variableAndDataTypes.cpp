#include <iostream>

using namespace std;

int main()
{
        //Declaring and initializing variable of different data types
        int myIntVar = 45;              // Integer (whole number without decimals)
        float myFloatVar = 23.4;        // Floating point number (with decimals)
        double myDoubleVar = 19.98;     // Floating point number (with decimals)
        char myCharVar = '#';           // Character
        string myStringVar = "Hello";   // String (text)
        bool myBoolVar = true;          // Boolean (true or false)

        // printing the values of variables
        cout << "The value of myIntVar is " << myIntVar <<endl;
        cout << "The value of myFloatVar is " << myFloatVar <<endl;
        cout << "The value of myDoubleVar is " << myDoubleVar <<endl;
        cout << "The value of myCharVar is " << myCharVar <<endl;
        cout << "The value of myStringVar is " << myStringVar <<endl;
        cout << "The value of myBoolVar is " << myBoolVar <<endl;
}

